class Barang:
    def __init__(self, category, name, num_buyers, price):
        # Attribute class (Category = kategori barang, 
        # name = nama barang
        # num_buyers = banyak orang yang membeli
        # price = harga untuk 1 buah barang
        # )
        self.category = category
        self.name = name
        self.num_buyers = num_buyers
        self.price = price
     
     
    